        public int Id { get; private set; }
        public string Value { get; private set; }

        public AnimationSlotType(int id, string value)
        {
            Id = id;
            Value = value.ToUpper();
        }

        public override string ToString()
        {
            return $"{Value}[{Id}]";
        }
